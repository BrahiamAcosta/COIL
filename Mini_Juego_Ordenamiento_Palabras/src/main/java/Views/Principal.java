    /**
     * @param args the command line arguments
     */
    public void WordsToLabel() throws IOException {
        jLabel1.setVisible(false);
        ArrayList<String> wordsList = new ArrayList<>();
        for (int i = 0; i < words.length(); i++) {
            String word = words.getString(i);
            wordsList.add(word);
        }
        Collections.shuffle(wordsList);
        JSONArray wordsDisorder = new JSONArray(wordsList);
        int rows = (int) Math.ceil(wordsDisorder.length() / 6.0);
        jPanel1.setLayout(new GridLayout(rows, 6));
        jPanel1.setLayout(new FlowLayout(FlowLayout.LEFT));
        jPanel1.setMinimumSize(new Dimension(460, 80));
        jPanel1.setPreferredSize(new Dimension(460, 80));
        jPanel1.setMaximumSize(new Dimension(460, 80));
        jPanel2.setLayout(new GridLayout(rows, 6));
        jPanel2.setLayout(new FlowLayout(FlowLayout.LEFT));
        jPanel2.setMinimumSize(new Dimension(460, 80));
        jPanel2.setPreferredSize(new Dimension(460, 80));
        jPanel2.setMaximumSize(new Dimension(460, 80));
        for (int i = 0; i < wordsDisorder.length(); i++) {
            JButton word = new JButton(wordsDisorder.getString(i));
            word.addActionListener(this);
            word.setBounds(10, 10, 200, 100);
            jPanel1.add(word);
        }
    }

    /**
     *
     * @param e
     */
    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() instanceof JButton button) {
            String buttonText = button.getText();
            JButton wordSelected = new JButton(buttonText);
            wordSelected.addActionListener(this);
            wordSelected.setBounds(10, 10, 200, 100);
            Container parent = button.getParent();
            if (parent == jPanel1) {
                jPanel1.remove(button);
                jPanel1.repaint();
                jPanel2.add(wordSelected);
                jPanel2.revalidate();
            } else if (parent == jPanel2) {
                jPanel2.remove(button);
                jPanel2.repaint();
                jPanel1.add(wordSelected);
                jPanel1.revalidate();
            }
            if (jPanel2.getComponentCount() == words.length()) {
                JSONArray sentence = new JSONArray();
                Component[] components = jPanel2.getComponents();
                for (Component component : components) {
                    if (component instanceof JButton) {
                        JButton word = (JButton) component;
                        String text = word.getText();
                        sentence.put(text);
                    }
                }
                StringBuilder sentenceTrue = new StringBuilder();
                StringBuilder sentenceUser = new StringBuilder();
                for (int i = 0; i < words.length(); i++) {
                    sentenceTrue.append(words.getString(i) + " ");
                    sentenceUser.append(sentence.getString(i) + " ");
                }
                Timer timer = new Timer(1000, (ActionEvent e1) -> {
                        // Aqu√≠ es donde ocultaremos el JLabel
                        jLabel1.setVisible(false);
                        try {
                            this.words = info.Words();
                            this.WordsToLabel();
                        } catch (IOException ex) {
                            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    });
                if (sentenceTrue.toString().equals(sentenceUser.toString())) {
                    String value = jLabel4.getText();
                    String plus = Integer.toString(Integer.parseInt(value) + 1);
                    jLabel4.setText(plus);
                    jLabel1.setText("Correct!");
                    jLabel1.setVisible(true);
                    timer.setRepeats(false);
                    timer.start();
                    jPanel2.removeAll();
                    jPanel2.repaint();
                } else {
                    jLabel1.setText("Incorrect!");
                    jLabel1.setVisible(true);
                    timer.setRepeats(false);
                    timer.start();
                    jPanel2.removeAll();
                    jPanel2.repaint();
                }
            }
        }
    }

